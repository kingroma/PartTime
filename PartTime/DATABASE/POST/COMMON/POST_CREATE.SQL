-- DROP TABLE TB_CO_USER ; 
CREATE TABLE TB_CO_USER (
	USER_ID VARCHAR(100) PRIMARY KEY , 
	USER_PW VARCHAR(100) ,
	USER_NM VARCHAR(100) ,
	USER_NICK VARCHAR(200) ,
	RM VARCHAR(1000) ,
	RGS_DT TIMESTAMP ,
	RGS_USER_ID VARCHAR(100) ,
	UPD_DT TIMESTAMP ,
	UPD_USER_ID VARCHAR(100) 
) ;

-- DROP TABLE TB_CO_USER_LOG ; 
CREATE TABLE TB_CO_USER_LOG (
	USER_ID VARCHAR(100) ,
	USER_PW VARCHAR(100) ,
	LOGIN_CODE VARCHAR(100) ,
	LOGIN_MSG VARCHAR(500) , 
	RGS_DT TIMESTAMP 
) ;