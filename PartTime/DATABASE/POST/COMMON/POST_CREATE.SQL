-- DROP TABLE TB_CO_USER ; 
CREATE TABLE TB_CO_USER (
	USER_ID VARCHAR(100) PRIMARY KEY , 
	USER_PW VARCHAR(100) ,
	USER_NM VARCHAR(100) ,
	USER_NICK VARCHAR(200) ,
	RM VARCHAR(1000) ,
	RGS_DT TIMESTAMP ,
	RGS_USER_ID VARCHAR(100) ,
	UPD_DT TIMESTAMP ,
	UPD_USER_ID VARCHAR(100) 
) ;

-- DROP TABLE TB_CO_USER_LOG ; 
CREATE TABLE TB_CO_USER_LOG (
	USER_ID VARCHAR(100) ,
	USER_PW VARCHAR(100) ,
	LOGIN_CODE VARCHAR(100) ,
	LOGIN_MSG VARCHAR(500) , 
	RGS_DT TIMESTAMP 
) ;

CREATE TABLE TB_CO_MENU (
	MENU_ID VARCHAR(100) ,
	MENU_NM VARCHAR(100) , 
	MENU_IMAGE_URL VARCHAR(1000) , 
	MENU_LEVEL VARCHAR(100) , 
	MENU_SORT_SN VARCHAR(100) ,
	MENU_URL VARCHAR(1000) 
) ;

CREATE TABLE TB_CT_VIDEO (
	VIDEO_ID VARCHAR(100) , 
	VIDEO_NM VARCHAR(1000) ,
	VIDEO_CN VARCHAR(4000) ,
	VIDEO_IMAGE VARCHAR(1000) ,
	VIDEO_URL VARCHAR(1000) , 
	VIDEO_MINI_URL VARCHAR(1000) ,
	
	VIDEO_CNT VARCHAR(100) ,
	RGS_DT TIMESTAMP , 
	UPD_DT TIMESTAMP  
);

CREATE TABLE TB_CT_VIDEO_GOOD_BAD(
	VIDEO_ID VARCHAR(100) , 
	USER_ID VARCHAR(100) , 
	GOOD_AT VARCHAR(1) ,
	BAD_AT VARCHAR(1) ,
	RGS_DT TIMESTAMP 
);

CREATE TABLE TB_CT_VIDEO_REPLY (
	VIDEO_ID VARCHAR(100) ,
	REPLY_ID VARCHAR(100) , 
	USER_ID VARCHAR(100) , 
	REPLY_TEXT VARCHAR(100) ,
	RGS_DT TIMESTAMP 
);



